def mul(first_arg, second_arg):
    result = first_arg * second_arg
    print(f"mul({first_arg}, {second_arg}) = {result}")
    return result

def add(first_arg, second_arg):
    result = first_arg + second_arg
    print(f"add({first_arg}, {second_arg}) = {result}")
    return result

def sub(first_arg, second_arg):
    result = first_arg - second_arg
    print(f"sub({first_arg}, {second_arg}) = {result}")
    return result
